
public class task114 {
    
    public static void main(String[] args) {
        for (int i=0; i <= 10; ++i){
            int a = i;
            if (a % 2 == 0){
                System.out.println(i + " is even.");
            } else {
                System.out.println(i + " is odd.");
            }
        }
    }
    
}
